package com.iva.dto.data;

public class UrlSource {

	private String creditPolicyUrl;
	private String loanDataUrl;
	private String marketDataUrl;

	public String getCreditPolicyUrl() {
		return creditPolicyUrl;
	}

	public void setCreditPolicyUrl(String creditPolicyUrl) {
		this.creditPolicyUrl = creditPolicyUrl;
	}

	public String getLoanDataUrl() {
		return loanDataUrl;
	}

	public void setLoanDataUrl(String loanDataUrl) {
		this.loanDataUrl = loanDataUrl;
	}

	public String getMarketDataUrl() {
		return marketDataUrl;
	}

	public void setMarketDataUrl(String marketDataUrl) {
		this.marketDataUrl = marketDataUrl;
	}

}
